// EOF 관련 문제
import java.util.*;

public class Main{
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        
        while(sc.hasNext()){
            int Num_A = sc.nextInt();
            int Num_B = sc.nextInt(); 
            
            System.out.println(Num_A+Num_B);
        }
    }
}